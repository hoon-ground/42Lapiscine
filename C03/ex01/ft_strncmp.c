int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while ((n > i) && (*s1 || *s2))
	{
		if ((*s1) > (*s2))
			return (*s1 - *s2);
		if ((*s1) < (*s2))
			return (*s1 - *s2);
		else
		{
			s1++;
			s2++;
			i++;
		}
	}
	return (0);
}
