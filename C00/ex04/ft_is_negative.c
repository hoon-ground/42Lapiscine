#include <unistd.h>

void	ft_is_negative(int n)
{
	char	buf1;
	char	buf2;

	buf1 = 'N';
	buf2 = 'P';
	if (n < 0)
		write(1, &buf1, 1);
	else if (n >= 0)
		write(1, &buf2, 1);
}
